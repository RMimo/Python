a = 10
b = 3

print('A:', a)
print('B:', b)

# addition
print('Addition:', a + b)

# subtraction
print('Subtraction:', a - b)

# multiplication
print('Multiplication:', a * b)

# division
print('Division:', a / b)

# modulus
print('Modulus:', a%b)

# exponentiation
print('Exponentiation:', a**b)

# floor division
print('Floor Division:', a//b)